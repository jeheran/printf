#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		len;
	int		i;
	char	*cpy;

	i = 0;
	len = 0;
	if (s1)
		len = ft_strlen(s1);
	if (!(cpy = malloc(sizeof(*s1) * (len + 1))))
		return (NULL);
	while (s1[i])
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
